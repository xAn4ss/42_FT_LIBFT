#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	int	x;

	x = 1;
	if (n == -2147483648)
		write(fd, "-2147483648", 11);
	else
	{
		if (n < 0)
		{
			ft_putchar_fd('-', fd);
			n = n * -1;
		}
		while (x <= (n / 10))
		{
			x *= 10;
		}
		while (x > 0)
		{
			ft_putchar_fd((n / x) + '0', fd);
			n = n % x;
			x /= 10;
		}
	}
}
